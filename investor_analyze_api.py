@app.route("/investor_analyze", methods=["POST"])
def analyze_investor():
    data = request.json
    name = data.get("name")
    gender = data.get("gender")
    dob = parser.parse(data.get("dob"))
    phone = data.get("phone")
    email = data.get("email")
    country = data.get("country")
    industry = data.get("industry")
    other_industry = data.get("other_industry", "")
    experience = data.get("experience")
    challenge = data.get("challenge")
    lang = data.get("lang", "en")

    age = datetime.now().year - dob.year
    final_industry = other_industry if industry == "Other" else industry

    chart_data = [
        {"label": "Brand Recall", "value": 92, "color": "#2b8be0"},
        {"label": "Client Fit Clarity", "value": 88, "color": "#f5a623"},
        {"label": "Trust Signals", "value": 85, "color": "#7ed321"}
    ]

    summary = (
        f"With over {experience} years of experience in {final_industry}, "
        f"this elite {gender.lower()} founder aged {age} based in {country} is actively seeking support in: {challenge}. "
        f"Based on global and regional founders of similar age, gender, and industry, this profile shows strong potential in high-value client attraction and investor confidence."
    )

    tips = [
        "ğŸš€ Elevate your brand image with high-quality content and visuals to attract elite clients/investors.",
        "ğŸ’° Showcase your past successes and client testimonials to build trust with potential investors.",
        "ğŸ“ˆ Set clear, measurable goals and communicate a strong business strategy for growth.",
        "ğŸŒŸ Highlight your unique selling points to stand out in a crowded market.",
        "ğŸ’¡ Offer exclusive perks to entice elite investors to choose your business.",
        "ğŸ¤ Build relationships with industry influencers and thought leaders.",
        "ğŸ“Š Provide transparent financial projections to show strong ROI.",
        "ğŸŒ Use SEO, social ads, and email campaigns to reach elite prospects.",
        "ğŸ¯ Tailor your messaging to match elite investor preferences.",
        "ğŸ† Celebrate milestones to inspire confidence and show your track record."
    ]

    html_report = format_html_report("ğŸ¯ Investor Positioning Snapshot", summary, chart_data, tips, lang)

    # Send email
    subject = "Your Investor Insight Report"
    message = MIMEText(html_report, "html")
    message["Subject"] = subject
    message["From"] = SMTP_USERNAME
    message["To"] = email

    try:
        smtp = smtplib.SMTP(SMTP_SERVER, SMTP_PORT)
        smtp.starttls()
        smtp.login(SMTP_USERNAME, SMTP_PASSWORD)
        smtp.sendmail(SMTP_USERNAME, [email], message.as_string())
        smtp.quit()
    except Exception as e:
        logging.error(f"Email failed: {e}")

    return jsonify({
        "title": "ğŸ¯ Investor Positioning Snapshot",
        "summary": summary,
        "charts": chart_data,
        "tips": tips
    })

